    private void crearTablaDatos() {
        try {
            // Limpiar las columnas existentes en el modelo de la tabla
            if (this.dftModeloTabla != null) {
                this.dftModeloTabla.setColumnCount(0);
                this.dftModeloTabla.addColumn("Fecha Venta");
                this.dftModeloTabla.addColumn("Marca Vehiculo");
                this.dftModeloTabla.addColumn("Modelo Vehiculo");
                this.dftModeloTabla.addColumn("Precio");
                this.dftModeloTabla.addColumn("ID Pedido");
                this.dftModeloTabla.addColumn("Fecha Venta");
                this.dftModeloTabla.addColumn("Nombre Cliente");
                this.dftModeloTabla.addColumn("Correo Cliente");
                this.dftModeloTabla.addColumn("Precio Venta");
            }
            this.repaint();
            //this.llenarTablaVehiculo();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void llenarTablaVehiculo() {
        this.dftModeloTabla.setRowCount(0);
        ArrayList<Compra>compras = this.vehiculoData.obtenerVehiculo(this.jTextField1.getText());
        for (Compra compra : compras) {
            Object[] rowData = {
                compra.getFechaVenta(),
                compra.getMarcaVehiculo(),
                compra.getModeloVehiculo(),
                compra.getPrecio(),
                compra.getIdPedido(),
                compra.getFechaVenta(),
                compra.getNombreCliente(),
                compra.getCorreoCliente(),
                compra.getPrecioVenta()
            };
            this.dftModeloTabla.addRow(rowData);
        }
        this.repaint();
    }

