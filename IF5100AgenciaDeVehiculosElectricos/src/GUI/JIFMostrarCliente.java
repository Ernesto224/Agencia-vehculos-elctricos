    private void crearTablaDatos() {
        try {
            // Limpiar las columnas existentes en el modelo de la tabla
            if (this.dftModeloTabla != null) {
                this.dftModeloTabla.addColumn("IDCliente");
                this.dftModeloTabla.addColumn("Identificacion");
                this.dftModeloTabla.addColumn("NombreCliente");
                this.dftModeloTabla.addColumn("Telefono");
                this.dftModeloTabla.addColumn("CorreoElectronico");
            }
            this.repaint();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void cargarDatos(){
        ArrayList<Cliente>clientes = new ArrayList<>();
        if (!this.jTextField1.getText().equals("")) {
            clientes = this.clienteData.obtenerClientes(Integer.parseInt(this.jTextField1.getText()));
        }else{
            clientes = this.clienteData.obtenerClientes(0);
        }
        for (Cliente cliente : clientes) {
            Object[] rowData = new Object[] {
                cliente.getIdCliente(),
                cliente.getIdentificacion(),
                cliente.getNombreCliente(),
                cliente.getTelefono(),
                cliente.getCorreo()
            };
            this.dftModeloTabla.addRow(rowData);
        }
    }
